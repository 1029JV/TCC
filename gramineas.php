            <h3 class="text-center"><strong>Gramíneas</strong></h3>

            <strong>Introdução</strong>
            <p class="recuo">As gramíneas são uma família de plantas com folhas semelhantes a lâminas. A maioria tem caule oco e muitas raízes ramificadas. A grama verde encontrada em jardins, a que cresce em campos, os cereais e os bambus pertencem à família das gramíneas.</p>

            <strong>Onde crescem as gramíneas</strong>
            <p class="recuo">As gramíneas crescem em quase todas as partes da Terra porque sobrevivem em climas muito diferentes. Grandes áreas de gramíneas selvagens são chamadas de campos ou pastagens. Entre os diferentes tipos de campos se encontram pradarias, estepes, savanas e pampas.</p>

            <strong>Características físicas</strong>
            <p class="recuo">Algumas gramíneas, como o capim-do-campo, são muito curtas. Outras, como o tipo de bambu chamado bambu-gigante, atingem a altura de 30 metros. As gramíneas geralmente são macias e flexíveis, mas os bambus são mais rijos e lenhosos.</p>
            <p class="recuo">As gramíneas possuem caules com junções ou nós. Uma folha brota de cada junção. Com exceção do milho, do sorgo e de algumas outras, as gramíneas têm caules ocos. Os caules de muitas gramíneas crescem rente ao chão, por isso praticamente só as folhas são visíveis.</p>
            <p class="recuo">Nas regiões tropicais, as gramíneas têm folhas largas. As que crescem em desertos têm folhas pontiagudas que ajudam a manter a umidade em seu interior. As folhas de algumas gramíneas são cerosas. A cerosidade as protege da água.</p>
            <p class="recuo">As gramíneas dependem do vento para espalhar o pólen e as sementes. Algumas sementes de gramíneas brotam rapidamente ao tocar o solo. Outras precisam congelar antes de brotar. As gramíneas também se espalham por meio de caules subterrâneos que crescem horizontalmente, produzindo novos brotos.</p>

            <strong>Usos</strong>
            <p class="recuo">Desde os tempos antigos, as pessoas comem as sementes de muitas gramíneas. Entre elas estão o milho, a cevada, o painço, a aveia, o arroz, o centeio, o sorgo e o trigo, chamados de cereais, ou grãos. Animais selvagens, bem como o gado doméstico, alimentam-se de gramíneas selvagens.</p>
            <p class="recuo">Depois que as sementes são retiradas dos cereais, restam os caules. Quando secos, esses caules são chamados de palha. Em algumas partes do mundo, as pessoas usam a palha para fazer telhados. Ela também é tecida para confeccionar móveis, esteiras, cestas e chapéus.</p>
            <p class="recuo">Os gramados são cultivados devido ao aspecto atraente que o verde dá aos jardins. Eles são também importantes para o meio ambiente. As raízes se espalham em todas as direções, o que mantém o terreno firme e previne a erosão.</p>
